package fcu.iecs.oop.tiida;

public class NissanTiida {	
	
	public int num;

	
	public void print(int num){
		
		int i=0 , j=0;
		
		for(i=0;i<num;i++)
		{
			for(j=0;j<num;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
	}
	
	
	
	
}
